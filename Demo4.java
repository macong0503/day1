public class Demo4 {
    public static void main(String[] args) {
        //算数运算符
        int a = 21;
        int b = 10;
        //取余
        int c = a % b;
        //除法
        int d = a / b;
        System.out.println(c);
        System.out.println(d);
        // ++ --
        int i = 1;
        System.out.println(i++);
        System.out.println(i);
        System.out.println("---------");
        System.out.println(++i);
        System.out.println(i);
        System.out.println("=========");
        int j = 5;
        System.out.println(j--);
        System.out.println(j);
        System.out.println(--j);
        System.out.println(j);
    }
}
