public class Demo5 {
    public static void main(String[] args) {
        //关系运算符
        int i = 1;
        int j = 2;
        //等于 ==
        boolean b1 = i == j;
        System.out.println(b1);
        //不等于 !=
        boolean b2 = i != j;
        System.out.println(b2);
        //大于 >
        boolean b3 = i > j;
        System.out.println(b3);
        //小于 <
        boolean b4 = i < j;
        System.out.println(b4);
    }
}
